package src.SelfLearning

object VarVal extends App{

  //Mutable variable
  var name: String = "Anjali Punyasri"
  name = "Kattunga"
  println(name)


  //Immutable variable
  val anotherName : String = "Anjali Punyasri"
  println(anotherName)


  val myVar : Int = 10
  val myVal : String = "Hello scala with datatype declaration"
  var myVar1 = 20
  var myVal1 = " Hello scala new without datatype declaration"
  println(myVar)
  println(myVal)
  println(myVar1)
  println(myVal1)
}
